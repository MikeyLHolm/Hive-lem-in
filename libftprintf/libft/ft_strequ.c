#include "libft.h"

/*
**	Bitwise version:
**
**	while (s1[0] != '\0' && !(*(s1) ^ *(s2)))
**	{
**		s1++;
**		s2++;
**	}
**	return (!(*(s1) ^ *(s2)));
**
** OLD VERSION
**
** while (s1[i] == s2[i])
**	{
**		if (s1[i] == '\0' && s2[i] == '\0')
**			return (1);
**		i++;
**	}
**	return (0);
** }
*/

int		ft_strequ(char const *s1, char const *s2)
{
	while (s1[0] != '\0' && !(*(s1) ^ *(s2)))
	{
		s1++;
		s2++;
	}
	return (!(*(s1) ^ *(s2)));
}
