#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		t;

	i = 0;
	t = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[t] != '\0')
	{
		s1[i] = s2[t];
		i++;
		t++;
	}
	s1[i] = '\0';
	return (s1);
}
