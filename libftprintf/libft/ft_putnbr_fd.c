#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long k;

	k = n;
	if (k < 0)
	{
		ft_putchar_fd('-', fd);
		k = k * -1;
	}
	if (k / 10 > 0)
	{
		ft_putnbr_fd(k / 10, fd);
	}
	ft_putchar_fd('0' + k % 10, fd);
}
